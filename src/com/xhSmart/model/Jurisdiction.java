package com.xhSmart.model;

public class Jurisdiction {
	private int jurisdiction_id;
	private String jurisdiction_name;
	private String jurisdiction_depict;
	public int getJurisdiction_id() {
		return jurisdiction_id;
	}
	public void setJurisdiction_id(int jurisdiction_id) {
		this.jurisdiction_id = jurisdiction_id;
	}
	public String getJurisdiction_name() {
		return jurisdiction_name;
	}
	public void setJurisdiction_name(String jurisdiction_name) {
		this.jurisdiction_name = jurisdiction_name;
	}
	public String getJurisdiction_depict() {
		return jurisdiction_depict;
	}
	public void setJurisdiction_depict(String jurisdiction_depict) {
		this.jurisdiction_depict = jurisdiction_depict;
	}
	
	
}
